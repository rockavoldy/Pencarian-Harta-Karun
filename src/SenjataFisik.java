public class SenjataFisik extends Senjata {
    public SenjataFisik(String idSenjata, String namaSenjata, int kekuatanSenjata) {
        super(idSenjata, namaSenjata, kekuatanSenjata);
    }

    @Override
    public String getIdSenjata() {
        return super.getIdSenjata();
    }

    @Override
    public String getNamaSenjata() {
        return super.getNamaSenjata();
    }

    @Override
    public int getKekuatanSenjata() {
        return super.getKekuatanSenjata();
    }
}
